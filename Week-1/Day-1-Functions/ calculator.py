# Day 1 - Functions
# Calculator Program

def add(a, b):
    return a + b

def subtract(a, b):
    return a - b

def div(a, b):
    return a / b

def multi(a, b):
    return a * b

def mod(a, b):
    return a % b

print(add(10, 5))
print(subtract(10, 5))
print(div(10, 5))
print(multi(10, 5))
print(mod(10, 5))
